package net.thevpc.halfa.api.model;

public class SizeD {
    private double width;
    private double height;

    public SizeD(double width, double height) {
        this.width = width;
        this.height = height;
    }

    public double getWidth() {
        return width;
    }

    public double getHeight() {
        return height;
    }
}
